DROP TABLE INQUIRY_ATTRIBUTE if EXISTS;
DROP TABLE INQUIRY if EXISTS;
DROP TABLE TOPIC if EXISTS;




CREATE MEMORY TABLE TOPIC (
  TOPIC_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  NAME VARCHAR(100) NOT NULL
);

CREATE MEMORY TABLE INQUIRY (
  INQUIRY_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  TOPIC_ID INTEGER NOT NULL FOREIGN KEY REFERENCES TOPIC (TOPIC_ID),
  DESCRIPTION VARCHAR(100) NOT NULL,
  CREATE_DATE DATE NOT NULL,
  CUSTOMER_NAME VARCHAR(100) NOT NULL
);

CREATE MEMORY TABLE INQUIRY_ATTRIBUTE (
  ATTRIBUTE_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  INQUIRY_ID INTEGER FOREIGN KEY REFERENCES INQUIRY (INQUIRY_ID),
  NAME VARCHAR(30) NOT NULL,
  VALUE VARCHAR(100) NOT NULL
);


